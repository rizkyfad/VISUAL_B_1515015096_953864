    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Font font;
        if(jCheckBox1.isSelected()){
            //Font Tebal
            font = new Font ("Monospaced", Font.BOLD,12);
        }else{
            font = new Font ("Monospaced", Font.PLAIN,12);
        }
        
        String JenisKelamin = null;
        if(jRadioButton1.isSelected())
            JenisKelamin = jRadioButton1.getText();
        else if (jRadioButton2.isSelected())
            JenisKelamin = jRadioButton2.getText();
        
        String nama = jTextField1.getText();
        String nim = jTextField2.getText();
        String kls = jTextField3.getText();
        String programstudi = jComboBox1.getSelectedItem().toString();
        String daerahasal = jComboBox2.getSelectedItem().toString();
        jTextArea1.setText(" Nama             : "+nama
                   +"\n NIM              : "+nim
                   +"\n Kelas            : "+kls
                   +"\n Jenis Kelamin    : "+JenisKelamin
                   +"\n Program Studi    : "+programstudi
                   +"\n Daerah Asal      : "+daerahasal);
        jTextArea1.setFont(font);
    }//GEN-LAST:event_jButton1ActionPerformed

